import mongoose from "mongoose";

const UserSchema = new mongoose.Schema(
  {
    firstName: {
      type: String,
      required: true,
      min: 2,
      max: 50,
    },
    lastName: {
      type: String,
      required: true,
      min: 2,
      max: 50,
    },
    email: {
      type: String,
      required: true,
      max: 50,
      unique: true,
    },
    password: {
      type: String,
      required: true,
      min: 5,
    },
    refresh_token: String,
    access_token: String,
  },
  { timestamps: true } //it will give automatically date once it's created
);
mongoose.pluralize(null);
const User = mongoose.model("User", UserSchema);
export default User;